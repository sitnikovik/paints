package background

import "github.com/sitnikovik/paints/internal/ansi"

const (
	// Red - the ANSI escape code for red background.
	Red ansi.Code = "\033[41m"
	// Green - the ANSI escape code for green background.
	Green ansi.Code = "\033[42m"
	// Yellow - the ANSI escape code for yellow background.
	Yellow ansi.Code = "\033[43m"
	// Blue - the ANSI escape code for blue background.
	Blue ansi.Code = "\033[44m"
	// Purple - the ANSI escape code for purple background.
	Purple ansi.Code = "\033[45m"
	// Cyan - the ANSI escape code for cyan background.
	Cyan ansi.Code = "\033[46m"
)
